#!/bin/sh
##### Paths of the script and other files
PROGDIR="/usr/local/synd"
PROG="/usr/local/synd/synd.sh"
IGNORE_IP_LIST=`sed -n '/^######/,$ p' $0|grep -v '######'`
CRON="/etc/cron.d/synd.cron"
CSF="/usr/sbin/csf"
IPT="/sbin/iptables"
##### frequency in minutes for running the script
##### Caution: Every time this setting is changed, run the script with --cron
#####          option so that the new frequency takes effect
FREQ=1
##### How many connections define a bad IP? Indicate that below.
NO_OF_CONNECTIONS=30
##### CSF_BAN=1 (Use csf for ban)
##### CSF_BAN=0 (Uses iptables for banning ips instead of CSF)
CSF_BAN=0
##### KILL=0 (Bad IPs are'nt banned, good for interactive execution of script)
##### KILL=1 (Recommended setting)
KILL=1
##### An email is sent to the following address when an IP is banned.
##### Blank would suppress sending of mails
EMAIL_TO=""
##### Number of seconds the banned ip should remain in blacklist.
BAN_PERIOD=0
head()
{
        echo "Syn-Deflate version 0.2 beta"
        echo "Based on Dos-Deflate - felosi <admin@nix101.com>"
        echo
}

showhelp()
{
        head
        echo 'Usage: synd.sh [OPTIONS] [N]'
        echo 'N : number of SYN_RECV connections (default 10)'
        echo 'OPTIONS:'
        echo '-h | --help: Show this help screen'
        echo '-c | --cron: Create cron job to run this script regularly (default 1 mins)'
        echo '-k | --kill: Block the offending ip making more than N SYN_RECV connections'
    echo 'syndc | Commmand to edit the configuration file with nano'
}

add_to_cron()
{
        rm -f $CRON
        sleep 1
        service crond restart
        sleep 1
        echo "SHELL=/bin/sh" > $CRON
        if [ $FREQ -le 2 ]; then
                echo "0-59/$FREQ * * * * root /usr/local/synd/synd.sh >/dev/null 2>&1" >> $CRON
        else
                let "START_MINUTE = $RANDOM % ($FREQ - 1)"
                let "START_MINUTE = $START_MINUTE + 1"
                let "END_MINUTE = 60 - $FREQ + $START_MINUTE"
                echo "$START_MINUTE-$END_MINUTE/$FREQ * * * * root /usr/local/synd/synd.sh >/dev/null 2>&1" >> $CRON
        fi
        service crond restart
}
while [ $1 ]; do
        case $1 in
                '-h' | '--help' | '?' )
                        showhelp
                        exit
                        ;;
                '--cron' | '-c' )
                        add_to_cron
                        exit
                        ;;
                '--kill' | '-k' )
                        KILL=1
                        ;;
                 *[0-9]* )
                        NO_OF_CONNECTIONS=$1
                        ;;
                * )
                        showhelp
                        exit
                        ;;
        esac
        shift
done
TMP_PREFIX='/tmp/synd'
TMP_FILE="mktemp $TMP_PREFIX.XXXXXXXX"
BANNED_IP_MAIL=`$TMP_FILE`
BANNED_IP_LIST=`$TMP_FILE`
echo "Banned the following ip addresses on `date`" > $BANNED_IP_MAIL
echo >> $BANNED_IP_MAIL
BAD_IP_LIST=`$TMP_FILE`
netstat -ntu | grep SYN_RECV | awk '{print $5}' | cut -d: -f1 | sort | uniq -c | sort -nr > $BAD_IP_LIST
cat $BAD_IP_LIST
if [ $KILL -eq 1 ]; then
        IP_BAN_NOW=0
        while read line; do
                CURR_LINE_CONN=$(echo $line | cut -d" " -f1)
                CURR_LINE_IP=$(echo $line | cut -d" " -f2)
                if [ $CURR_LINE_CONN -lt $NO_OF_CONNECTIONS ]; then
                        break
                fi
                IGNORE_BAN=`grep -c $CURR_LINE_IP $IGNORE_IP_LIST`
                if [ $IGNORE_BAN -ge 1 ]; then
                        continue
                fi
                IP_BAN_NOW=1
                echo "$CURR_LINE_IP with $CURR_LINE_CONN SYN_RECV connections" >> $BANNED_IP_MAIL
                echo $CURR_LINE_IP >> $BANNED_IP_LIST
                echo $CURR_LINE_IP >> $IGNORE_IP_LIST
                if [ $CSF_BAN -eq 1 ]; then
                        $CSF -d $CURR_LINE_IP
                else
                        $IPT -I INPUT -s $CURR_LINE_IP -j DROP
                fi
        done < $BAD_IP_LIST
        if [ $IP_BAN_NOW -eq 1 ]; then
                dt=`date`
                hn=`hostname`
                if [ $EMAIL_TO != "" ]; then
                        cat $BANNED_IP_MAIL | mail -s "IP addresses banned on $dt $hn" $EMAIL_TO
                fi
        fi
fi
rm -f $TMP_PREFIX.*
exit 0
